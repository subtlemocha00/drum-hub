const yt = (q) =>
  `https://www.youtube.com/results?search_query=${encodeURIComponent(q + ' drum groove')}`

function grv(id, name, style, difficulty, bpm, timeSignature, description, opts = {}) {
  return {
    id,
    name,
    style,
    difficulty,
    bpm,
    timeSignature,
    description,
    practiceNotes: opts.practiceNotes || '',
    relatedRudiments: opts.rudiments || [],
    videoUrl: opts.videoUrl || yt(name)
  }
}

